let products = ['Apple','Orange','Banana','Lemon','Jack Fruit'];

console.log("********** Using For loop *********: ");
for(let i=0; i<products.length; i++){ // For loop
console.log(products[i]);
}


console.log("********* Using While loop *********: ");
let j=0;

while(j< products.length){  // while loop
    console.log(products[j]);
    j++;
}